                <p class="section-title">OVERVIEW</p><hr>
                <div>
                    Producing electricity from plants is a powerful, established concept evident in up-and-coming research. Past methods have involved the use of nanotechnology or natural soil chemistry.
                    These studies demonstrate both feasibility and ability to commercialize.  However, previous investigations are limited by their low power generation, 
                    interference with natural plant processes, and reliance on the inefficiency of the photosynthetic pathway.<br><br>
                    In contrast, this project proposes a technique that could harness higher levels of power with minimal ecological impact.  The process will not rely on photosynthesis 
                    and will be harmlessly integrated into the plant’s biological system and surrounding environment.  This research hinges upon nanogenetics and the use of the plant’s own carbon storage 
                    or other biological pathways to create the nanoparticles of the coating and bioelectric network.  In comparison to solar panels, this project has the potential to be cheaper, less toxic, 
                    and more efficient by using natural materials and not requiring constant maintenance.<br><br>
