    <h3 class="theme-text">BIOSOLAR CELL</h3>
    <hr />
    <h4 class="theme-text">Project Overview</h4>
    <p>
      Vespa orientalis is a middle eastern wasp that is the only known organism in the animal kingdom that can convert light to electricity.  Researchers at Tel Aviv University have completed over thirty years of research establishing the biological phenomenon of the electrical capabilities of the hornet.  These include the ability to convert waste heat to electricity, store charge, and have an efficiency rate of 40-60%.  All of this is done with a chitin exoskeleton, a biodegradable biopolymer, that is normally an insulating material but here demonstrates semiconducting effects.  Research indicates that the yellow cuticle within the hornet is primarily responsible for these mechanisms, but we suspect that the geometry and chemical composition of the cuticle also play a role.  
    </p>
    <p>
      The objective of the BioSolar Cell project is to better understand the underlying mechanism of this cuticle to synthetically recreate the multifunctional solar cell.  Commercial solar cells are 12-13% efficient, lose voltage with an increase in temperature, and rely on toxic materials in production and manufacturing.  By taking inspiration from nature, we hope to bring to the world a new type of solar cell that tackles three major problems in the solar tech industry.
